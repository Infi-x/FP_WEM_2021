    private void ejecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ejecutarActionPerformed
        if (this.antiguo.getText().length() == 0 || this.campoD.getText().length() == 0 || this.tablaD.getText().length() == 0
                || this.variableD.getText().length() == 0)
            JOptionPane.showMessageDialog(null, "No deje datos vacios", "Error", JOptionPane.INFORMATION_MESSAGE);
        else{
            this.con.update(campoD.getText().toString(), variableD.getText().toString(), antiguo.getText().toString(), tablaD.getText().toString());
            dispose();
        } 
    }//GEN-LAST:event_ejecutarActionPerformed

